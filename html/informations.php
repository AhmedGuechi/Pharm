    <div class="paragraph">
        <p>La publicité pour les produits pharmaceutiques en direction des professionnels de santé consiste en toute
            activité de promotion de la prescription et de la délivrance des produits pharmaceutiques. Elle est soumise
            à l’autorisation préalable de l’agence nationale des produits pharmaceutiques et ne peut être effectuée que
            pour les produits pharmaceutiques régulièrement enregistrés.</p>
        <p>La publicité ne doit pas être trompeuse ni porter atteinte à la protection de la santé publique. Elle doit
            présenter le médicament ou produit de façon objective et favoriser son bon usage. Elle doit respecter les
            dispositions de la décision d’enregistrement ainsi que les stratégies thérapeutiques recommandées par le
            ministère de la santé. </p>
        <p>La publicité pour un médicament est interdite lorsque le médicament fait l’objet d’une réévaluation du
            rapport entre les bénéfices et les risques.</p>
        <p>La publicité pour les produits pharmaceutiques et la promotion en direction du public sont interdites quels
            que soient les moyens d’information utilisés.</p>
        <h3>Dossier à fournir pour une demande de visa de publicité</h3>
        <p>La demande d’un visa de publicité se fait auprès de l’agence des produits pharmaceutique en deux temps :</p>
        <ol>
            <li class="L">Paiement de la redevance</li>
            <ul>
                <li>Une lettre adressée au directeur général de l’agence nationale des produits pharmaceutiques
                    précisant le nom et le type de l’établissement pharmaceutique demandeur (fabricant, importateur,
                    distributeur, exploitant, société de promotion médicale), le cas échéant, le mandant, le nom du
                    produit, sa dénomination commune internationale, son dosage et sa ou ses présentations. Elle est
                    signée par le pharmacien responsable de l’information médicale.</li>
                <li>Bordereau de versement de la redevance dûment renseigné. Le montant de la redevance pour le visa de
                    publicité est de 60 000 DA par produit, par dosage et par présentation. (à télécharger ici)</li>
                <li>S’il s’agit de la première demande de visa, une copie de l’agrément de promotion (pour les
                    établissements pharmaceutiques d’importation, de distribution, d’exploitation et les sociétés
                    spécialisées dans la promotion médicale), et le cas échéant, du contrat d’exploitation.</li>

      </ul>
      <p>Le bordereau de versement cosigné par le directeur des finances et de la comptabilité et celui et des
        contrôles et de l’expertise est retiré par le demandeur au niveau du bureau d’ordre moyennant accusé de
        réception. Le paiement de la redevance s’effectue par virement au profit de l’agence nationale des
        produits pharmaceutiques au RIB indiqué sur le bordereau de versement.</p>
      <li class="L">
        <h4>Dépôt du dossier de demande de visa</h4>
      </li>
      <p>Une fois acquitté des frais de la redevance ; le demandeur dépose le dossier de demande de visa de
        publicité au niveau du bureau d’ordre de l’agence nationale des produits pharmaceutiques. Le dossier
        comporte les pièces suivantes :</p>
      <ul>
        <li>La lettre de présentation adressée au directeur général de l’agence nationale des produits
          pharmaceutiques indiquant les destinataires de la publicité et le mode de diffusion. Elle précise en
          outre, le ou les produit(s) pharmaceutique(s) concerné(s). Elle est signée par le pharmacien
          responsable de l’information médicale.</li>
        <li>Le formulaire de visa de publicité dûment renseigné</li>
        <li>Une copie de la preuve de paiement</li>
        <li>Une copie de la décision d’enregistrement en cours de validité. Dans le cas où la décision
          d’enregistrement a expiré, un récépissé de dépôt de renouvellement est joint.</li>
        <li>La maquette de la publicité en couleur, elle doit être visible et claire.</li>
        <li>Un CD-ROM contenant une version numérisée des pièces susmentionnées (1, 2, 3, 4, 5).</li>
        <li>Le cas échéant, une copie en version numérique des références bibliographiques : les études venant
          étayer un résultat, une propriété ou une allégation liée au produit, quelle qu’en soit la
          méthodologie (étude clinique, préclinique, de pharmacocinétique, etc.) doivent être fournies, en
          français ou en anglais, dans leur version originales ou traduites, avec surlignage en jaune des
          éléments mentionnés dans le projet de publicité.</li>

      </ul>

    </ol>
