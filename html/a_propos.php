      relative à la santé, article 1er.
    </p>
    <h2>Missions Principales</h2>
    <p>
      <strong>Enregistrement des produits pharmaceutiques</strong> et octroi de la décision d'enregistrement , de son
      renouvellement, de sa suspension, de son retrait, de sa cession et de son transfert, après avis de la commission
      d'enregistrement des produits pharmaceutiques.<br>
      <strong>Homologation des dispositifs médicaux</strong> et l'octroi de la décision d'homologation, de son
      renouvellement, de sa suspension, de son retrait, de sa cession et de son transfert, après avis de la commission
      d'homologation des dispositifs médicaux.<br>
      <strong>Contrôle de la qualité et de l'expertise</strong> des produits pharmaceutiques et des dispositifs médicaux
      et la tenue des substances étalons et des produits de référence à l'échelle nationale.<br>
      <strong>Effectuer des missions d'audits et d'inspections</strong> sur sites réalisées par des inspecteurs relevant
      de l'agence et portant, notamment sur le contrôle de l'application des règles de bonnes pratiques pharmaceutiques
      et les normes des dispositifs médicaux, conformément à la législation et à la réglementation en vigueur.<br>
      <strong>Contribuer à l'élaboration</strong> des stratégies de développement du secteur pharmaceutique.<br>
      <strong>Saisir les autorités compétentes</strong> afin de prendre les mesures nécessaires visant à préserver la
      santé publique lorsqu'un produit pharmaceutique ou un dispositif médical présente ou est soupçonné de présenter un
      danger pour la santé humaine.<br>
      <strong>Emettre un avis</strong> sur les autorisations temporaires d'utilisation (ATU) de médicaments non
      enregistrés.<br>
      <strong>Contribuer à la définition des règles de bonnes pratiques</strong> de fabrication, de stockage, de
      distribution et de dispensation des produits pharmaceutiques.<br>
      <strong>Procéder à l'évaluation scientifique</strong> des bénéfices, des risques et de la valeur thérapeutique des
      produits pharmaceutiques et des dispositifs médicaux ainsi qu'à leur évaluation médico-économique.<br>
      <strong>De contribuer à l'établissement des nomenclatures</strong> des produits pharmaceutiques et des dispositifs
      médicaux et à leur actualisation.<br>
      <strong>De contribuer à l'élaboration</strong> de la liste des produits pharmaceutiques et des dispositifs
      médicaux essentiels.<br>
      <strong>De contribuer à l'élaboration</strong> du formulaire national des médicaments et de la pharmacopée.<br>
      <strong>De délivrer l'attestation des prix</strong> des médicaments à l'enregistrement, une fois fixés par le
      comité économique intersectoriel des médicaments.<br>
      <strong>De participer à l'élaboration</strong> de la liste des médicaments remboursables par les organismes de
      sécurité sociale.<br>
      <strong>De délivrer les autorisations préalables</strong> de promotion et de publicité des produits
      pharmaceutiques enregistrés en direction des professionnels de la santé.<br>
      <strong>D'émettre un avis sur les demandes</strong> de réalisation des études cliniques et des études de
      bioéquivalence.<br>
      <strong>D'émettre un avis</strong> sur les normes, les règles de bonnes pratiques, les procédures et les méthodes
      applicables aux études cliniques portant sur les produits pharmaceutiques et les dispositifs médicaux.<br>
      <strong>D'entreprendre</strong> toute étude, recherche, action de formation ou d'information dans les domaines de
      sa compétence et de contribuer à la promotion de la recherche scientifique dans le domaine des produits
      pharmaceutiques et des dispositifs médicaux et de constituer les bases de données y afférentes.<br>
      <strong>D'organiser</strong> des séminaires, des colloques, des journées d'études et autres manifestations en
      relation avec ses missions.<br>
      <strong>De participer à l'élaboration</strong> des projets de textes législatifs et réglementaires régissant les
      produits pharmaceutiques et les dispositifs médicaux.<br>
      <strong>De mettre en œuvre</strong> les actions de coopération internationale, conformément à la législation et à
      la réglementation en vigueur.<br>
      <strong>D'établir un rapport annuel</strong> sur ses activités qu'elle adresse au ministre chargé de l'industrie
      pharmaceutique.<br>

    </p>

