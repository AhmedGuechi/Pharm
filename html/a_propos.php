    <h2>Création de l'Agence Nationale des Produits Pharmaceutiques</h2>
    <p>Par <strong>loi n°18-11 du 2 juillet 2018</strong> relative à la santé, en son<strong> article 223</strong>.<br>
      L'agence est un établissement public à gestion spécifique doté de la personnalité morale et de l'autonomie
      financière, placé sous la tutelle du ministère de la santé »<br>
      <strong>(Art 224 de la loi n°18-11).</strong><br>
      Cette tutelle a été transférée vers le<strong> <a class="links" href="https://www.miph.gov.dz/fr/" target="_blank"
          rel="noreferrer noopener"> ministére de l'industrie pharmaceutique</strong> </a>par <strong>l'ordonnance
